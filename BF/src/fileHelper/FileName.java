package fileHelper;
/**
 * 文件名字类
 * 
 * @author SilverNarcissus
 */
public class FileName {
	String fileName;
	
	public FileName(String fileName) {
		this.fileName = fileName;
	}

	public void setFileName(String fileName) {
		this.fileName = fileName;
	}

	public String getFileName() {
		return fileName;
	}
}
